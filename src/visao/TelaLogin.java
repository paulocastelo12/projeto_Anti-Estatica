    private void btAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAcessarActionPerformed
        String login = jTLogin.getText();
        String senha = jTSenha.getText();

        Usuario usuario = new Usuario();

        UsuarioDAO usuarioDao = new UsuarioDAO();

        try {
            usuario = usuarioDao.validarLogin(login, senha);

            if (usuario != null) {
                //JOptionPane.showMessageDialog(null, "entrou");
                TelaPrincipal tp = new TelaPrincipal(usuario);
                tp.setVisible(true);
                dispose();
            } else {
                jLInvalidos.setText("Senha ou Login Incorretos!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btAcessarActionPerformed

